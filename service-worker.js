// 2. Fetch Event: Serving content from cache or network
self.addEventListener('fetch', (event) => {
  event.respondWith(
    caches.match(event.request)
      .then((response) => {
        // Cache hit - return response
        if (response) {
          return response;
        }
        // No cache hit - fetch from network
        return fetch(event.request)
          .catch(() => {
            // This catch handles network failures (e.g., truly offline)
            // If you have an offline page, return it here:
            // return caches.match('/offline.html');
            console.log('Network request failed and no cache match for:', event.request.url);
            // Optionally, return a default offline page or error message
            // For a fully contained app, this might not be strictly necessary if all assets are cached.
          });
      })
  );
});

// 3. Activate Event: Cleaning up old caches
self.addEventListener('activate', (event) => {
  const cacheWhitelist = [CACHE_NAME];
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheWhitelist.indexOf(cacheName) === -1) {
            // Delete old caches
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});